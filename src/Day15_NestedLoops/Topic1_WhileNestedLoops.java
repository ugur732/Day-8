package Day15_NestedLoops;

public class Topic1_WhileNestedLoops {
    public static void main(String[] args) {


        int i=0;
        while (i<10){
            System.out.println(i);
            int j=1;
            while (j<10){
                System.out.println("for each i: "+i+" value "+" the j value: "+j );
                j++;
            }
           i++;
        }














    }
}
